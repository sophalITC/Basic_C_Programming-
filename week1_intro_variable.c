#include <stdio.h>
#include <math.h>

int main(){
    // int (%d), float/double (%f)
    printf(" %d\n", 5+6);
    printf(" %d\n",3*6);
    printf(" %d\n", 10/4);
    printf(" %f\n", 10.5 *2);
    printf(" %f\n",10/3.0);
    printf(" %d\n", 10.0/6);
    printf(" %f\n",10.0/5+7*2-8+9); // *,/, +,- (--a,a--,b++,++b)
    printf(" %f\n", pow(2,4));

}
